#include <iostream>
using namespace std;

int main() {
    int a, b;

    // Input two numbers from the user
    cout << "Enter the first number: ";
    cin >> a;
    cout << "Enter the second number: ";
    cin >> b;

    // Calculate the sum
    int sum = a + b;

    // Output the result
    cout << "The sum is: " << sum << endl;

    return 0;
}
